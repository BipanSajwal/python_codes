#Slicing means picking up only the required strings
# indexing means the pointing the exact strings
# Strings count started from 0
a="Hello everyone welcome to python class"
print(a[10]) #indexing example
print(a[-15])
print(a[23])

print(a[2:29])
print(a[0:35:2]) #(0-starting , 35= stop ,2= jump or skip

print(a[-1])
print(a[-25])
print(a[1:-25])
print(a[1::4])